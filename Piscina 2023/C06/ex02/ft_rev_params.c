#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	o;

	i = argc - 1;
	o = 0;
	if (argc > 0)
	{
		while (i > 0)
		{
			while (argv[i][o] != '\0')
			{
				write(1, &argv[i][o], 1);
				o++;
			}
			i--;
			o = 0;
			write(1, "\n", 1);
		}
	}
}
